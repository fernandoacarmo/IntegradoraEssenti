        public static List<Estreplicacao_Ecommerce> LoadECommerce(string where)
        {
            List<Estreplicacao_Ecommerce> Lista;
            try
            {
                string filtro;
                filtro = Integradora.Biblioteca.Dictionaries.Settings.QuantidadeRegistroEnviar().ToString();
                if (!string.IsNullOrEmpty(filtro)) 
                {
                   filtro = "ROWNUM <= " + filtro;
                }

                if (!string.IsNullOrEmpty(where))
                {
                    filtro = filtro + " AND " + where;
                }

                Lista = Estreplicacao_Ecommerce.Listar(filtro);
            }
            catch
            {
                Lista = null;
            }
            return Lista;
        }

